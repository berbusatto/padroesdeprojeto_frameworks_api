public class Calcula {
    public double soma(float n1, float n2) {
        return n1 + n2;
    }

    public double subtrai(float n1, float n2) {
        return n1 - n2;
    }

    public double multiplica(float n1, float n2) {
        return n1 * n2;
    }

    public double divide(float n1, float n2) {
        return n1 / n2;
    }
}
